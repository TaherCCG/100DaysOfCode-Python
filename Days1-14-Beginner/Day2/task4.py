# Number Manipulation and F Strings

bmi = 84 / 1.65 ** 2
print(bmi)

# Flooring a number
print(int(bmi))
# Rounding a number
print(round(bmi))

# Assignment Operator
score = 0
score += 5
print(score)
score -= 1
print(score)
score *= 2
print(score)
score /= 2

# f-String
name = "Taher"
age = 46
print(f"My name is {name} and I'm {age} years old.")

